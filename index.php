<h1>Print an Binder with Instapaper</h1>
<p>This is an example PHP script for pulling bookmarked pages from Instapaper, creating a PDF from them, and printing as binder with Mimeo.</p>
<br />
<p>This uses combination of 3 APIs:</p>
<ul>
<li><a href="http://www.instapaper.com/api/full">Instapaper API</a></li>
<li><a href="https://pdfcrowd.com/html-to-pdf-api/">PDF Crowd API</a></li>
<li><a href="http://developer.mimeo.com">Mimeo Connect Cloud Print API</a></li>
</ul>
